package com.example.guswn.workoutlog;

public class Statistics_Another_Info {
    String ST2_part;
    String ST2_name;
    String ST2_reps;

    public Statistics_Another_Info(String ST2_part, String ST2_name, String ST2_reps) {
        this.ST2_part = ST2_part;
        this.ST2_name = ST2_name;
        this.ST2_reps = ST2_reps;
    }

    public String getST2_part() {
        return ST2_part;
    }

    public void setST2_part(String ST2_part) {
        this.ST2_part = ST2_part;
    }

    public String getST2_nane() {
        return ST2_name;
    }

    public void setST2_nane(String ST2_nane) {
        this.ST2_name = ST2_nane;
    }

    public String getST2_reps() {
        return ST2_reps;
    }

    public void setST2_reps(String ST2_reps) {
        this.ST2_reps = ST2_reps;
    }
}
